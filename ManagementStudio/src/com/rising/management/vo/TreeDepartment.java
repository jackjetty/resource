package com.rising.management.vo;

import java.util.ArrayList;

public class TreeDepartment {
	
	public String text;
	
	public String id;
	
	//是否选中
	private Boolean checked;
	//是否打开
	private String state;	
	//子菜单
	private ArrayList<TreeDepartment> children;
	

	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public ArrayList<TreeDepartment> getChildren() {
		return children;
	}
	public void setChildren(ArrayList<TreeDepartment> children) {
		this.children = children;
	}
	public Boolean getChecked() {
		return checked;
	}
	public void setChecked(Boolean checked) {
		this.checked = checked;
	}
	
	
}
