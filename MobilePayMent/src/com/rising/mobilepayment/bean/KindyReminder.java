package com.rising.mobilepayment.bean;

public class KindyReminder {

	private String id;
	private String productName;
	private String chargeTipTitle;
	private String chargeTipContent;
	private String confirmTipTitle;
	private String confirmTipContent;
	private Integer hidden;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getChargeTipTitle() {
		return chargeTipTitle;
	}

	public void setChargeTipTitle(String chargeTipTitle) {
		this.chargeTipTitle = chargeTipTitle;
	}

	public String getChargeTipContent() {
		return chargeTipContent;
	}

	public void setChargeTipContent(String chargeTipContent) {
		this.chargeTipContent = chargeTipContent;
	}

	public String getConfirmTipTitle() {
		return confirmTipTitle;
	}

	public void setConfirmTipTitle(String confirmTipTitle) {
		this.confirmTipTitle = confirmTipTitle;
	}

	public String getConfirmTipContent() {
		return confirmTipContent;
	}

	public void setConfirmTipContent(String confirmTipContent) {
		this.confirmTipContent = confirmTipContent;
	}

	public Integer getHidden() {
		return hidden;
	}

	public void setHidden(Integer hidden) {
		this.hidden = hidden;
	}

}
