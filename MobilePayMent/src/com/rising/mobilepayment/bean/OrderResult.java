package com.rising.mobilepayment.bean;

public class OrderResult {
	private String businessType;
	private String rechargeType;
	private String vipType;
	private String recordId;
	private String rechargeTime;
	private String rechargeNumber;
	private String rechargeResult;
	private Integer parvalue;
	private float orderAmount;

	public String getBusinessType() {
		return businessType;
	}

	public void setBusinessType(String businessType) {
		this.businessType = businessType;
	}

	public String getRechargeType() {
		return rechargeType;
	}

	public void setRechargeType(String rechargeType) {
		this.rechargeType = rechargeType;
	}

	public String getVipType() {
		return vipType;
	}

	public void setVipType(String vipType) {
		this.vipType = vipType;
	}

	public String getRecordId() {
		return recordId;
	}

	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}

	public String getRechargeTime() {
		return rechargeTime;
	}

	public void setRechargeTime(String rechargeTime) {
		this.rechargeTime = rechargeTime;
	}

	public String getRechargeNumber() {
		return rechargeNumber;
	}

	public void setRechargeNumber(String rechargeNumber) {
		this.rechargeNumber = rechargeNumber;
	}

	public String getRechargeResult() {
		return rechargeResult;
	}

	public void setRechargeResult(String rechargeResult) {
		this.rechargeResult = rechargeResult;
	}

	public Integer getParvalue() {
		return parvalue;
	}

	public void setParvalue(Integer parvalue) {
		this.parvalue = parvalue;
	}

	public float getOrderAmount() {
		return orderAmount;
	}

	public void setOrderAmount(float orderAmount) {
		this.orderAmount = orderAmount;
	}

}
