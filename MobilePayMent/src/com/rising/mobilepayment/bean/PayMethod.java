package com.rising.mobilepayment.bean;

import java.util.Date;

public class PayMethod {
	// 渠道id
	private Integer payMethodId;
	// 渠道信息：一般指所属商户
	private String payMethodDetail;
	// 渠道信息：一般指商户应用环境
	private String remark;
	// 渠道所属商户id
	private String merchantId;

	// 渠道使用限制：是否进行ip地址绑定
	private String ipBind;

	// 渠道绑定ip
	private String bindIp;

	// 渠道是否需要预下单
	private String perOrder;

	// 渠道预留信息，用于身份验证
	private String leftInfo;

	// 渠道是否进行用户身份验证
	private String userCheck;

	// 渠道身份验证接口地址
	private String userCheckUrl;

	private String status;

	private Date createTime;

	private Date deleteTime;

	public Integer getPayMethodId() {
		return payMethodId;
	}

	public void setPayMethodId(Integer payMethodId) {
		this.payMethodId = payMethodId;
	}

	public String getPayMethodDetail() {
		return payMethodDetail;
	}

	public void setPayMethodDetail(String payMethodDetail) {
		this.payMethodDetail = payMethodDetail;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getMerchantId() {
		return merchantId;
	}

	public void setMerchantId(String merchantId) {
		this.merchantId = merchantId;
	}

	public String getIpBind() {
		return ipBind;
	}

	public void setIpBind(String ipBind) {
		this.ipBind = ipBind;
	}

	public String getBindIp() {
		return bindIp;
	}

	public void setBindIp(String bindIp) {
		this.bindIp = bindIp;
	}

	public String getPerOrder() {
		return perOrder;
	}

	public void setPerOrder(String perOrder) {
		this.perOrder = perOrder;
	}

	public String getLeftInfo() {
		return leftInfo;
	}

	public void setLeftInfo(String leftInfo) {
		this.leftInfo = leftInfo;
	}

	public String getUserCheck() {
		return userCheck;
	}

	public void setUserCheck(String userCheck) {
		this.userCheck = userCheck;
	}

	public String getUserCheckUrl() {
		return userCheckUrl;
	}

	public void setUserCheckUrl(String userCheckUrl) {
		this.userCheckUrl = userCheckUrl;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getDeleteTime() {
		return deleteTime;
	}

	public void setDeleteTime(Date deleteTime) {
		this.deleteTime = deleteTime;
	}

}
