package com.rising.mobilepayment.bean;
import java.util.Date;
public class QQPool{
	
	private String qqNumber;
	private String qqPassword;
	private Integer qbBalance;
	private Date lastCheckTime;
	private Date lastChargeTime;
	private String qqStatus;
	private Date createTime;
	private String remark;
	public String getQqNumber() {
		return qqNumber;
	}
	public void setQqNumber(String qqNumber) {
		this.qqNumber = qqNumber;
	}
	public String getQqPassword() {
		return qqPassword;
	}
	public void setQqPassword(String qqPassword) {
		this.qqPassword = qqPassword;
	}
	public Integer getQbBalance() {
		return qbBalance;
	}
	public void setQbBalance(Integer qbBalance) {
		this.qbBalance = qbBalance;
	}
	public Date getLastCheckTime() {
		return lastCheckTime;
	}
	public void setLastCheckTime(Date lastCheckTime) {
		this.lastCheckTime = lastCheckTime;
	}
	public Date getLastChargeTime() {
		return lastChargeTime;
	}
	public void setLastChargeTime(Date lastChargeTime) {
		this.lastChargeTime = lastChargeTime;
	}
	public String getQqStatus() {
		return qqStatus;
	}
	public void setQqStatus(String qqStatus) {
		this.qqStatus = qqStatus;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	 
}