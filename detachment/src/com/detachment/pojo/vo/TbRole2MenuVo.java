package com.detachment.pojo.vo;

import com.detachment.pojo.TbRole2menu;

public class TbRole2MenuVo {
	private String roleId;
	private String menuId;
	private Boolean valid;
	
	public TbRole2MenuVo(TbRole2menu rm){
		this.roleId=rm.getId().getTbRole().getRoleId();
		this.menuId=rm.getId().getTbMenu().getMenuId();
		this.valid=rm.getValid();
	}
	
	
	public String getRoleId() {
		return roleId;
	}
	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public Boolean getValid() {
		return valid;
	}
	public void setValid(Boolean valid) {
		this.valid = valid;
	}
	
	
	
	
}
