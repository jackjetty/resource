package com.detachment.wei.bean.store.info;

import java.io.Serializable;

public class  PictureStoreInfo implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 2500395663666851968L;
	private String url;
	private String type;
	private String info;
	private String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	
}