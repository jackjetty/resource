package com.detachment.wei.bean.store.info;

import java.io.Serializable;

public class VoiceStoreInfo implements Serializable{
		/**
	 * 
	 */
	private static final long serialVersionUID = 4049575042238095179L;
		private String url;
		private String type;
		private String info;
		public String getUrl() {
			return url;
		}
		public void setUrl(String url) {
			this.url = url;
		}
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public String getInfo() {
			return info;
		}
		public void setInfo(String info) {
			this.info = info;
		}
		
	}